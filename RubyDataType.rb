7.Hash类型

三.数据类型转换

  ruby的整数、浮点数、字符串的类均提供了to_i,to_f,to_s三个方法，分别用于转换成整数、转换成
浮点数、转换成字符串

四.类型判断
    (1)instance_of? 方法用来判断对象是否是一个类的实例，会忽略继承
    ==============================================================
            class A;      end
            class B < A ;  end
         
            b = B.new
            b.instance_of? A  #=> false
            b.instance_of? B   #=> true
    ==============================================================

    (2)is_a?  kinde_of?这个两个方法都用来判断对象是否是一个类的实例，包括超类和module
    ==============================================================
            module M;    end
            class A
            include M
            end
            class B < A; end
       
            b = B.new
            b.is_a? A          #=> true
            b.is_a? B          #=> true
            b.is_a? M          #=> true  
    ==============================================================

五.引用传递
  在ruby中一切都是对象，而你向方法中传递的实质上是对象的引用（ object-reference）。ruby中变量都
是对象的引用。所有ruby为引用传递
  
